st.markdown("### üìê –ö–∞–ª—å–∫—É–ª—è—Ç–æ—Ä –æ–±—ä–µ–º–∞ –ø–æ SL (%)")
st.info("‚ö†Ô∏è –≠—Ç–æ—Ç –±–ª–æ–∫ –Ω–µ –≤–ª–∏—è–µ—Ç –Ω–∞ —Å–∏–º—É–ª—è—Ü–∏—é ‚Äî –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è —Ç–æ–ª—å–∫–æ –¥–ª—è —Ä–∞—Å—á—ë—Ç–∞ –≤—Ä—É—á–Ω—É—é")

with st.expander("üîç –ü–æ–∫–∞–∑–∞—Ç—å"):
    st.write("–†–∞—Å—Å—á–µ—Ç –æ–±—ä–µ–º–∞ –ø–æ–∑–∏—Ü–∏–∏ –ø—Ä–∏ —Ñ–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–Ω–æ–º —Ä–∏—Å–∫–µ –∏ —Ä–∞–∑–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏—è—Ö SL (%)")

    dep_sl = st.number_input("üíº –î–µ–ø–æ–∑–∏—Ç ($)", value=100.0, min_value=0.0, key="slcalc_dep")
    risk_pct_input = st.number_input(
        "üî• –§–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–Ω—ã–π —Ä–∏—Å–∫ –Ω–∞ —Å–¥–µ–ª–∫—É (%)",
        value=2.0, min_value=0.01, max_value=100.0, step=0.01, key="slcalc_risk_pct"
    )

    # –ë–∞–∑–æ–≤—ã–µ –ø—Ä–æ–≤–µ—Ä–∫–∏
    if dep_sl <= 0:
        st.warning("–£–∫–∞–∂–∏ –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω—ã–π –¥–µ–ø–æ–∑–∏—Ç.")
    else:
        # 2) –û—Ç–æ–±—Ä–∞–∂–∞–µ–º —Ñ–∏–∫—Å. —Ä–∏—Å–∫ –Ω–∞ —Å–¥–µ–ª–∫—É –≤ $ (–ø—Ä—è–º –Ω–∞–ø—Ä–æ—Ç–∏–≤ %)
        risk_dollar_direct = dep_sl * risk_pct_input / 100.0  # –ø—Ä–æ—Å—Ç–æ –ø–µ—Ä–µ–≤–æ–¥ –≤–≤–µ–¥—ë–Ω–Ω–æ–≥–æ % –≤ –¥–æ–ª–ª–∞—Ä—ã

        # 3) –ú–∞–∫—Å. –¥–æ–ø—É—Å—Ç–∏–º–∞—è –ø–æ—Ç–µ—Ä—è –¥–ª—è —Å—Ç–æ–ø-—Ç–æ—Ä–≥–æ–≤ –≤ % (–ø–æ —Ç–≤–æ–µ–π —Ñ–æ—Ä–º—É–ª–µ): —Ä–∏—Å–∫% / 4
        max_loss_pct_stop = risk_pct_input / 4.0

        # 4) –ü–æ—Ç–µ—Ä—è –≤ $ –ø—Ä–∏ —Å—Ç–æ–ø-—Ç–æ—Ä–≥–∞—Ö (—Ñ–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–Ω–∞—è): –¥–µ–ø–æ–∑–∏—Ç √ó (–º–∞–∫—Å.–ø–æ—Ç–µ—Ä—è%/100)
        max_loss_dollar_stop = dep_sl * (max_loss_pct_stop / 100.0)

        # 5) –†–∏—Å–∫ –Ω–∞ —Å–¥–µ–ª–∫—É ($), –∏—Å–ø–æ–ª—å–∑—É–µ–º—ã–π –≤ —Ç–∞–±–ª–∏—Ü–µ: –º–∞–∫—Å.–¥–æ–ø—É—Å—Ç–∏–º–∞—è –ø–æ—Ç–µ—Ä—è / 4
        #    (—Ç.–∫. –¥–æ–ø—É—Å–∫–∞–µ–º 4 —É–±—ã—Ç–æ—á–Ω—ã–µ –ø–æ–¥—Ä—è–¥, –∫–∞–∂–¥–∞—è ‚Äî 1/4 –æ—Ç –ª–∏–º–∏—Ç–∞ —Å—Ç–æ–ø-—Ç–æ—Ä–≥–æ–≤)
        risk_per_trade_dollar_for_table = max_loss_dollar_stop / 4.0

        # –í—ã–≤–æ–¥–∏–º –∫–ª—é—á–µ–≤—ã–µ –º–µ—Ç—Ä–∏–∫–∏ ¬´–Ω–∞–ø—Ä–æ—Ç–∏–≤¬ª
        c1, c2, c3, c4 = st.columns(4)
